export function getHintPuzzle(puzzle) {
  let sum = 0;
  let puzzleHint = {
    left: [],
    top: [],
  };

  puzzle.forEach(() => puzzleHint.left.push([]));
  puzzle[0].forEach(() => puzzleHint.top.push([]));

  puzzle.forEach((row, rowIndex) => {
    let sum = 0;
    row.forEach((num, colIndex) => {
      if (num === 1) {
        sum += 1;
        if (colIndex === row.length - 1) {
          puzzleHint.left[rowIndex].push(sum);
        }
      } else if (sum > 0) {
        puzzleHint.left[rowIndex].push(sum);
        sum = 0;
      }
    });
    if (puzzleHint.left[rowIndex].length === 0) {
      puzzleHint.left[rowIndex].push(0);
    }
  });

  for (let colIndex = 0; colIndex < puzzle[0].length; colIndex += 1) {
    sum = 0;
    for (let rowIndex = 0; rowIndex < puzzle.length; rowIndex += 1) {
      if (puzzle[rowIndex][colIndex] === 1) {
        sum += 1;
        if (rowIndex === puzzle.length - 1) {
          puzzleHint.top[colIndex].push(sum);
        }
      } else if (sum > 0) {
        puzzleHint.top[colIndex].push(sum);
        sum = 0;
      }
    }

    if (puzzleHint.top[colIndex].length === 0) {
      puzzleHint.top[colIndex].push(0);
    }
  }
  return puzzleHint;
}
